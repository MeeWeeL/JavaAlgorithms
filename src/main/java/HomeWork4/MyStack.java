package HomeWork4;

import HomeWork4.lesson.LinkedList;

public class MyStack<E> implements Stack<E> {

    private final MyLinkedList<E> data;

    public MyStack() {
        this.data = new MyLinkedListImpl<>();
    }

    @Override
    public void push(E value) {
        data.insertFirst(value);
    }

    @Override
    public E pop() {
        return data.removeFirst();
    }

    @Override
    public E peek() {
        return data.getFirst();
    }

    @Override
    public int size() {
        return data.size();
    }

    @Override
    public boolean isEmpty() {
        return data.isEmpty();
    }

    @Override
    public boolean isFull() {
        return false;
    }

    @Override
    public void display() {
        data.display();
    }
}
